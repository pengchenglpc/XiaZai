package com.lpc.xiazai.vo;

public class XiaZaiContextVo {
	private String id;
	private String url;
	private String tmpPath;
	private String downloadClass;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getTmpPath() {
		return tmpPath;
	}
	public void setTmpPath(String tmpPath) {
		this.tmpPath = tmpPath;
	}
	public String getDownloadClass() {
		return downloadClass;
	}
	public void setDownloadClass(String downloadClass) {
		this.downloadClass = downloadClass;
	}
	
}
